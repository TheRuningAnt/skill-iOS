#import "JobModel.h"

@implementation JobModel

-(void)setValue:(id)value forUndefinedKey:(NSString *)key{
    
    if([key isEqualToString:@"description"]){
        self.jobDescription = value;
    }
    if ([key isEqualToString:@"id"]) {
        self.jobId = [value integerValue];
    }
}

@end
